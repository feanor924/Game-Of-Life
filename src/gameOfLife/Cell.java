public class Cell {

    private int coorX;
    private int coorY;

    public Cell(){

    }

    public void setCoorX(int X){

        coorX=X;
    }

    public void setCoorY(int Y){

        coorY=Y;
    }

    public int getCoorX(){

        return coorX;
    }

    public int getCoorY(){

        return coorY;
    }



}
